# Create a hash using both Ruby syntax styles

# old syntax
stanley = {
  :animal => "cat",
  :age => 15
}

# new syntax
mama = {
  animal: "cat",
  age: 9
}

# Remember that the newer syntax only works if our keys are symbols. 
# If our keys are any other data type, we'll need to use the older 
# syntax (using the hash rocket).
